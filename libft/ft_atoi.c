#include "libft.h"

long	ft_atoi(const char *str)
{
	int		i;
	long	nbr;
	int		negative;

	nbr = 0;
	negative = 0;
	i = 0;
	if (str[i] == '-')
		negative = 1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] && ('9' >= str[i] && str[i] >= '0'))
	{
		nbr *= 10;
		nbr += str[i] - '0';
		i++;
	}
	if (negative == 1)
		return (-nbr);
	else
		return (nbr);
}
