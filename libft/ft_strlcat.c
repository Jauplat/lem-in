#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	lendest;
	size_t	lensrc;

	i = 0;
	lendest = ft_strlen(dest);
	lensrc = ft_strlen((char *)src);
	if (size < lendest)
		return (size + lensrc);
	while (src[i] != '\0' && (lendest + i + 1) < size)
	{
		dest[lendest + i] = src[i];
		i++;
	}
	dest[lendest + i] = '\0';
	return (lendest + lensrc);
}
