#include "lem_in.h"

int		room_cmp(char *s1, char *s2)
{
	int		i;
	int		j;

	i = 0;
	if (s1 == NULL || s2 == NULL)
		return (1);
	while (s1[i])
	{
		j = 0;
		if (s1[i + j] == s2[j] && (i == 0 || s1[i - 1] == ' '))
		{
			while (s2[j] == s1[i + j])
			{
				if (!s2[j + 1] && (!s1[i + j + 1] || s1[i + j + 1] == ' '))
					return (0);
				j++;
			}
		}
		i++;
	}
	return (1);
}
